    private void leeDatos(File alum) {
        try {
            String dato;
            int ind=0;
            BufferedReader lector = new BufferedReader(new FileReader(alum.getPath()));
            while((dato=lector.readLine())!=null) {
                dato=dato.trim();
                switch (ind) {
                    case 0:
                           txtNombre.setText(dato);
                        break;
                    case 1:
                            switch (dato) {
                            case "ISC":
                                    comboBoxCarreras.setSelectedIndex(0);
                                break;
                            case "Informatica":
                                    comboBoxCarreras.setSelectedIndex(1);
                                break;
                            case "TIC":
                                    comboBoxCarreras.setSelectedIndex(2);
                                break;
                            }
                        break;
                    case 2:
                            if(dato.equalsIgnoreCase("Masculino")){
                                btnMasculino.setSelected(true);
                            }else{
                                btnFemenino.setSelected(true);
                            }
                        break;
                }
                ind++;
            }
            lector.close();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    private void guardarDatos(String numCont) {
         try {
            File alum = new File(numCont);
            BufferedWriter bw = new BufferedWriter(new FileWriter("Alumnos\\"+alum,false));
            bw.write(txtNombre.getText());
            bw.newLine();
            bw.write(""+comboBoxCarreras.getSelectedItem());
            bw.newLine();
            bw.write(btnMasculino.isSelected()?"Masculino":"Femenino");
            bw.close();
            JOptionPane.showMessageDialog(null,"Guardado");
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

