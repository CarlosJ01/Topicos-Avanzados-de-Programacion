    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
       File alum=null;
       String numCon=txtNumeroControl.getText();
       numCon=numCon.trim();
       if(!numCon.isEmpty() && numCon.length()>=8){
           File cpt = new File("Alumnos");
           for (File arv:cpt.listFiles()) {
               if (arv.getName().equalsIgnoreCase(numCon)) {
                   alum=arv;
                   break;
               }
           }
           
           //Verifica si encontro el archivo
           if(alum!=null){
               leeDatos(alum);
           } else{
               JOptionPane.showMessageDialog(null,"No existe el alumno");
           }
       }
    }//GEN-LAST:event_btnBuscarActionPerformed

