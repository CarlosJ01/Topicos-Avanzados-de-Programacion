    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        try {
            //Abrir el archivo
            File arv = new File("Publicaciones.json");
            if (!arv.exists()) {
                BufferedWriter escritor = new BufferedWriter(new FileWriter(arv));
                escritor.write("[]");
                escritor.close();
            }
            leer();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_formWindowOpened

    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        JSONObject nuevoProducto = new JSONObject();
        nuevoProducto.put("Nombre",txtNombre.getText());
        nuevoProducto.put("Precio",Double.parseDouble(txtPrecio.getText()));
        nuevoProducto.put("Cantidad",Integer.parseInt(txtCantidad.getText()));
        //System.out.println(comboBoxImagenes.getSelectedItem().toString());
        nuevoProducto.put("Imagen",comboBoxImagenes.getSelectedItem().toString());
        
        JSONObject nuevoVendedor = new JSONObject();
        nuevoVendedor.put("Nombre","Alverto");
        nuevoVendedor.put("Correo","Alverto01@gmail.com");
        nuevoVendedor.put("Reputacion",9.5);
        
        JSONObject nuevaPublicacion = new JSONObject();
        nuevaPublicacion.put("Producto",nuevoProducto);
        nuevaPublicacion.put("Vendedor",nuevoVendedor);
        Random ram=new Random();
        nuevaPublicacion.put("Tipo",ram.nextInt(3));
        
        publicaciones.add(nuevaPublicacion);
        
        try {
            BufferedWriter escritor = new BufferedWriter(new FileWriter("Publicaciones.json",false));
            escritor.write(publicaciones.toJSONString());
            escritor.close();
        } catch (Exception e) {
            e.printStackTrace();
        }
        JOptionPane.showMessageDialog(null,"Guardado Satisfactoriamente");
    }//GEN-LAST:event_btnGuardarActionPerformed

