    private void leer() {
        try {
            BufferedReader lector=new BufferedReader(new FileReader("Publicaciones.json"));
            String cadena="";
            String dato=null;
            while ((dato=lector.readLine())!=null) {                
                cadena+=dato;
                //System.out.println(cadena);
            }
            lector.close();
            JSONParser parser = new JSONParser();
            publicaciones=(JSONArray)parser.parse(cadena);//Guardar la cadena extraida en el arrego de JSON
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
