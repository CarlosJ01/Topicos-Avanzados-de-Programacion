    private void mostrar() {
        if(ind == rutas.size()){
            ind=0;
        }else{
            if(ind==-1){
                ind=rutas.size()-1;
            }
        }
        String tipo="";
        ruta = (JSONObject)rutas.get(ind);
        switch (ruta.get("Tipo").toString()) {
            case "1":
                    tipo="Platino";
                break;
            case "2":
                    tipo="Gold";
                break;
            case "3":
                    tipo="Silver";
                break;
            default:
                throw new AssertionError();
        }
        txtTipo.setText(tipo);
        txtLugares.setText(ruta.get("Lugares").toString());
    }

    private void escribir() {
        try {
            BufferedWriter escritor = new BufferedWriter(new FileWriter("Rutas.json",false));
            escritor.write(rutas.toString());
            escritor.close();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
