    //Guardar el alumno
    private void btnGuardarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnGuardarMouseClicked
        Alumno alumno = new Alumno();
        alumno.setNumeroControl(textNumeroControl.getText());
        alumno.setNombreCompleto(textNombreCompleto.getText());
        alumno.setSemestre(Integer.parseInt(jSpinner1.getValue().toString()));
        alumno.setCarrera(new Carrera(idCarrera,nombreCarrera));
        
        try {
            ManejadorDB manejador = new ManejadorDB();
            if (manejador.nuevoAlumno(alumno)>0) {
                JOptionPane.showMessageDialog(null,"Se inscribio un nuevo alumno a la carrera de \""+nombreCarrera);
            }else{
                JOptionPane.showMessageDialog(null,"Hubo un error, intentelo m√°s tade");
            }
            this.dispose();
        } catch (Exception e) {
            e.printStackTrace();
            System.out.println("Error al ingresar alumno");
        }
    }//GEN-LAST:event_btnGuardarMouseClicked

